        "                    \"Lon:\", plane.get(\"lon\")\n",
        "                )\n",
        "\n",
        "        else:\n",
        "            print(\"API Error:\", response.status_code)\n",
        "\n",
        "\n",
        "    # Show all emergency aircraft on one map\n",
        "    create_map(emergency_planes)\n"
