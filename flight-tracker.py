        "    print(\"C) 7700: Mechanical/Other Failure\")\n",
        "    print(\"D) All Emergencies\")\n",
        "\n",
        "    squawk_type = input(\n",
        "        \"Which squawk would you like to track: \"\n",
        "    ).lower()\n",
        "\n",
        "\n",
        "    if squawk_type == \"a\":\n",
        "        codes = [\"7500\"]\n",
        "\n",
        "    elif squawk_type == \"b\":\n",
        "        codes = [\"7600\"]\n",
