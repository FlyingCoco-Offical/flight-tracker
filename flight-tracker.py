        "    else:\n",
        "        print(\"Airline not found\")\n",
        "\n",
        "    flightnum = int(input(\"Enter the flight number: \"))\n",
        "    url = f\"https://api.adsb.lol/v2/callsign/{callsign}{flightnum}\"\n",
        "\n",
        "    response = requests.get(url)\n",
        "\n",
